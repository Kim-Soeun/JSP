package dto;

public class ClothDTO {

	private String clothId;
	private String clothType;
	private String clothSize;
	private int clothPrice;
	private String clothFile;
	
	
	
	public String getClothId() {
		return clothId;
	}
	public void setClothId(String clothId) {
		this.clothId = clothId;
	}
	public String getClothType() {
		return clothType;
	}
	public void setClothType(String clothType) {
		this.clothType = clothType;
	}
	public String getClothSize() {
		return clothSize;
	}
	public void setClothSize(String clothSize) {
		this.clothSize = clothSize;
	}
	public int getClothPrice() {
		return clothPrice;
	}
	public void setClothPrice(int clothPrice) {
		this.clothPrice = clothPrice;
	}
	public String getClothFile() {
		return clothFile;
	}
	public void setClothFile(String clothFile) {
		this.clothFile = clothFile;
	}
	
	
	
	
}
